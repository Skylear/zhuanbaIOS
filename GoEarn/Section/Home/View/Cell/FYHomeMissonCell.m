- (void)configCollectionView {
    // 如不需要长按排序效果，将LxGridViewFlowLayout类改成UICollectionViewFlowLayout即可
    LxGridViewFlowLayout *layout = [[LxGridViewFlowLayout alloc] init];
    //每个cell的间距
    _margin = 5;
    
    //每一行的上下距离
    CGFloat minimum2 = 5;
    //每个cell的宽
    CGFloat cellW = (SCREEN_WIDTH -30- _margin *(MaxRowCount -1))/MaxRowCount ;
    
    _itemWH = cellW;
    
    layout.itemSize = CGSizeMake(cellW, _itemWH);
    
    // 设置每一行的间距
    layout.minimumLineSpacing = minimum2;
    
    // 设置每个cell的间距
    layout.minimumInteritemSpacing = _margin;
    
    // 设置每组的内边距
    layout.sectionInset = UIEdgeInsetsMake(0, 5, 0, 5);
    int _Row = _inRow>=3 ?3:_inRow;
    _collectionView = [[UICollectionView alloc] initWithFrame:CGRectMake(0, 0, SCREEN_WIDTH, (_itemWH+5)*_Row +10) collectionViewLayout:layout];
    DLog(@"deletecollectionProjectHeigh===%f",_itemWH*_Row +10);
    _collectionView.scrollEnabled = NO;
    _collectionView.alwaysBounceVertical = YES;
    _collectionView.backgroundColor = [UIColor whiteColor];
    _collectionView.contentInset = UIEdgeInsetsMake(4, 4, 4, 4);
    _collectionView.dataSource = self;
    _collectionView.delegate = self;
    _collectionView.keyboardDismissMode = UIScrollViewKeyboardDismissModeOnDrag;
    [self addSubview:_collectionView];
    [_collectionView registerClass:[TZTestCell class] forCellWithReuseIdentifier:@"TZTestCell"];
}

