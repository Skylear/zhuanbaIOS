#pragma mark - Click Event

- (void)deleteBtnClik:(UIButton *)sender {
    [_selectedPhotos removeObjectAtIndex:sender.tag];
    [_selectedAssets removeObjectAtIndex:sender.tag];
    [_collectionView performBatchUpdates:^{
        NSIndexPath *indexPath = [NSIndexPath indexPathForItem:sender.tag inSection:0];
        [_collectionView deleteItemsAtIndexPaths:@[indexPath]];
        _inRow =[self returnCount:_selectedPhotos];
        _collectionView.boundsHeight = (_itemWH+5)*_inRow +10;
        if ([_delegate respondsToSelector:@selector(registCellDeleteRow:withSource:withRow:)]) {
            [self.delegate registCellDeleteRow:self withSource:_selectedPhotos withRow:_inRow];
        }
    } completion:^(BOOL finished) {
        [_collectionView reloadData];
    }];
}
